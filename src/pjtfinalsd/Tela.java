    public Tela() {
            File f = new File(System.getProperty("user.dir") + System.getProperty("file.separator")  + "arquivosDisponiveis.txt");
            FileReader fr;
            try {
                fr = new FileReader(f);
                BufferedReader br = new BufferedReader(fr);
                String st, arquivos=""; 

                while ((st = br.readLine()) != null && st.contains(";")) {
                    arquivos = st;
                }
                arq = Arrays.asList(arquivos.split(";"));
                
                
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            initComponents();
            setLocation(500, 250);
            
            //adicionar lista de arquivos no jList
            DefaultListModel listModel = new DefaultListModel();
         
            for (String percorrer : arq) {
               listModel.addElement(percorrer);
            }
            jList1.setModel(listModel);
        
        
    }

